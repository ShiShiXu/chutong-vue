      $(function () {
        let $timelineItem = $('.timeline-item');
        let $tlItemLength = $timelineItem.length;
        let $timeline = $('.timeline');

        // 根据 timeline-item 个数算出 timeline 的具体宽度 （页面布局用）
        let tlwidth = $tlItemLength * 360 + ($tlItemLength - 1) * 52;
        $timeline.css('width', tlwidth);
        // 根据 timeline-item 按钮单击最大次数
        let times = Math.floor($tlItemLength / 3) + 1;
        let flag = 0;
        // 全局的位移数据 0
        let nextNumber = 0;
        // 每次点击next箭头，位移量 -744
        $('.next').on('click', function() {
          if (nextNumber > times * (-824)) {
            nextNumber = nextNumber - 824;
            let nextTranslateX = 'translateX(' + nextNumber + 'px)';
            $timeline.css('transform', nextTranslateX);
            flag++;
            console.log('判断基准：' + times * (-824));
            console.log('当前位移：' + nextNumber);
            $('.prev').css('opacity', '1');
          } else {}
        });
        $('.prev').on('click', function() {
          if (nextNumber === 0) {
            return false;
          } else {
            nextNumber = nextNumber + 824;
            let nextTranslateX = 'translateX(' + nextNumber + 'px)';
            $timeline.css('transform', nextTranslateX);
            console.log('判断基准：' + 0);
            console.log('当前位移：' + nextNumber);
            $('.prev').css('opacity', '1');
          }
        });
      });
    }
  };
</script>

